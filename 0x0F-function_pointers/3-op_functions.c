#include "3-calc.h"

/**
 * op_add - adds two intger
 * @a: the frist int
 * @b: the second int
 *
 * Return: the sum
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two intgers
 * @a: the frist int
 * @b: the second int
 *
 * Return: the difference
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies two intgers
 * @a: the frist int
 * @b: the second int
 *
 * Return: the product
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two intgers
 * @a: the frist int
 * @b: the second int
 *
 * Return: the result of the division
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two intgers
 * @a: the frist int
 * @b: the second int
 *
 * Return: the remainder
*/
int op_mod(int a, int b)
{
	return (a % b);
}
